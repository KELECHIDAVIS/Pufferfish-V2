double evaluate(const Board *board) {

    int whoToMove = board->whiteToMove ? 1 : -1;
    double score = 0; // for rn only counting material score 
    int pieceWeights[] = {100, 320, 330, 500, 900, 20000}; // p,n,b,r,q,k
    
    U64 allPieces = getAllPieces(board); 
    while(allPieces){
        enumSquare sq= LSBIT(allPieces); 
        allPieces = CLEARLSBIT(allPieces); 
        enumPiece piece = board->mailbox[sq];
        if (piece == nKing || !isValidPiece(piece))
            continue; // kings will be decided later to see if were currently in late game

        // if white add, if black minus 
        U64 pieceBit = 1ULL << sq; 
        int pieceIndx = piece-2; 
        
        if (getColorPieces(board , nWhite ) & pieceBit){
            score +=( pieceWeights[pieceIndx ] + pieceSquareTable[pieceIndx][sq]) ; 
        }else{
            score -= (pieceWeights[pieceIndx] + pieceSquareTable[pieceIndx][63-sq]); // have to flip the table and make it neg for black 
        }


    }

    return score;
}